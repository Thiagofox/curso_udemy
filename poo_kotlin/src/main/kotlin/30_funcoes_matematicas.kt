import kotlin.math.*

fun main() {

    // Máximo entre dois números
    println(max(10, 100))

    // Mínimo entre dois números
    println(min(10, 100))

    // Raíz quadrada
    println(sqrt(2.0))

    // PI
    println(PI)

    // e - Euler
    println(E)

    // Arredonta para número mais próximo
    println(round(15.456))

    // Formatação de casas decimais
    val n1 = 15254.4567492
    println("%.2f".format(n1))

}


